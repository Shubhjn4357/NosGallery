const fs = require('fs');
const path = require('path');

const widgetsDir = path.resolve(__dirname, '../src/widgets');
const genFilePath = path.resolve(__dirname, '../modules/expo-widget/src/widgetRegistry.gen.ts');

function walkDir(dir, callback) {
  if (!fs.existsSync(dir)) return;
  fs.readdirSync(dir).forEach(f => {
    const dirPath = path.join(dir, f);
    const isDirectory = fs.statSync(dirPath).isDirectory();
    if (isDirectory) {
      walkDir(dirPath, callback);
    } else {
      callback(dirPath);
    }
  });
}

const discovered = [];

walkDir(widgetsDir, (filePath) => {
  if (!filePath.endsWith('.tsx') && !filePath.endsWith('.ts')) return;
  if (filePath.includes('widgetRenderer.tsx') || filePath.includes('registry.ts')) return;

  const content = fs.readFileSync(filePath, 'utf8');
  
  // Find all @widget directives
  const widgetRegex = /\/\/\s*@widget\s+([a-zA-Z0-9_-]+)/g;
  let match;
  const ids = [];
  while ((match = widgetRegex.exec(content)) !== null) {
    ids.push(match[1]);
  }
  
  if (ids.length === 0) return;
  
  // Find the exported component name
  const exportMatch = content.match(/export\s+(const|function|class)\s+([a-zA-Z0-9_]+)/);
  if (!exportMatch) {
    console.warn(`[Registry Generator] Warning: Found @widget directive in ${filePath} but no export statement!`);
    return;
  }
  
  const componentName = exportMatch[2];
  const fileRelPath = path.relative(widgetsDir, filePath).replace(/\\/g, '/');
  
  ids.forEach(id => {
    discovered.push({
      id,
      componentName,
      fileRelPath: `../../../src/widgets/${fileRelPath}`.replace(/\.tsx?$/, '')
    });
  });
});

// Group by component to consolidate imports
const importsMap = {};
discovered.forEach(d => {
  if (!importsMap[d.fileRelPath]) {
    importsMap[d.fileRelPath] = new Set();
  }
  importsMap[d.fileRelPath].add(d.componentName);
});

// Generate imports
let code = `// This file is auto-generated by scripts/generate-widget-registry.js.
// Do not edit this file manually. Any changes will be overwritten.
import React from 'react';\n\n`;

Object.keys(importsMap).sort().forEach(importPath => {
  const components = Array.from(importsMap[importPath]).sort().join(', ');
  code += `import { ${components} } from '${importPath}';\n`;
});

code += `\nexport function getWidgetComponent(templateId: string): React.ComponentType<any> {\n`;
code += `  switch (templateId) {\n`;

discovered.sort((a, b) => a.id.localeCompare(b.id)).forEach(d => {
  code += `    case '${d.id}':\n`;
  code += `      return ${d.componentName};\n`;
});

code += `    default:\n`;
code += `      throw new Error(\`[WidgetRegistry] Unknown widget template ID: \${templateId}\`);\n`;
code += `  }\n`;
code += `}\n`;

fs.writeFileSync(genFilePath, code, 'utf8');
console.log(`[Registry Generator] Generated widgetRegistry.gen.ts at ${genFilePath} with ${discovered.length} widgets.`);
